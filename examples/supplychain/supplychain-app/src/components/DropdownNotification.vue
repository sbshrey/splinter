<template>
  <div :class="{ 'new' : !notification.read }"
       class="dropdown-notification"
       @click="markAsRead">
    <div class="text-wrapper">
      <span class="text">
        <span>{{ formatText(notification) }}</span>
      </span>
      <div class="meta-wrapper">
        <i class="icon material-icons-round">
          {{ icons[notification.notification_type] }}
        </i>
        <span class="timestamp">
          {{ fromNow(notification.timestamp) }}
        </span>
      </div>
    </div>
  </div>
</template>

<script lang="ts">
import { Vue, Component, Prop } from 'vue-property-decorator';
import * as moment from 'moment';
import { SupplychainNotification, Supplychain } from '@/store/models';
import { hashGameName } from '@/utils/xo-games';


@Component
export default class DropdownNotification extends Vue {
  icons = {
    invite: 'share',
  };

  @Prop()
  notification!: SupplychainNotification;

  get link(): any {
    const regex = RegExp('^new_game_created');
    const notification: string = regex.test(
      this.notification.notification_type) ?
      'new_game_created' :
      this.notification.notification_type;
    switch (notification) {
      case('supplychain_proposal'): return {name: 'invitations'};
      case('circuit_active'):
        return {
          name: 'supplychains',
          params: {
            id: `${this.notification.target}`,
          },
        };
      case('new_game_created'):
        return {
          name: 'games',
          params: {
            id: `${this.notification.target}`,
            gameNameHash: `${hashGameName(this.getGameName(this.notification.notification_type))}`,
          },
        };
      default: return '';
    }
  }

  getName(): string {
    const supplychains = this.$store.getters['supplychains/supplychainList'];
    const supplychain = supplychains.find((g: Supplychain) => g.circuit_id === this.notification.target);
    return supplychain.alias;
  }

  formatText(notification: SupplychainNotification) {
    const regex = RegExp('new_game_created:');
    if (regex.test(notification.notification_type)) {
      const gameName = this.getGameName(notification.notification_type);
      return  `A new game is available in supplychain ${this.getName()}: ${gameName}`;
    }
    switch (notification.notification_type) {
      case('supplychain_proposal'): {
        return `${notification.requester_org} has invited you to a new supplychain: ${this.getName()}`;
      }
      case('circuit_active'): {
        return `A new supplychain has been created: ${this.getName()}`;
      }
      default: return '';
    }
  }

  getGameName(notificationType: string): string {
    const regex = RegExp('new_game_created:');
    if (regex.test(notificationType)) {
      const gameName = notificationType.split('new_game_created:')[1];
      return  gameName;
    } else {
      return '';
    }
  }

  fromNow(timestamp: number): string {
    return moment.unix(timestamp).fromNow();
  }

  markAsRead() {
    this.$store.dispatch('notifications/markRead', this.notification.id);
    this.$router.push(this.link);
    this.$emit('click');
  }
}
</script>


<style lang="scss" scoped>
@import '@/scss/components/_dropdown-notification.scss';
